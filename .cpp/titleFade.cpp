#include "titleFade.h"
#include "renderer.h"
#include "manager.h"
#include "scene2D.h"

//==================================================================================================================
// マクロ定義
//==================================================================================================================
#define DCREACE_ALPHA 0.05f			// 透明度の減少させる値

//==================================================================================================================
// 静的メンバ変数宣言
//==================================================================================================================
LPDIRECT3DTEXTURE9 CTitleFade::m_pTexture = NULL;					// テクスチャ情報
CTitleFade::TITLE_FADE CTitleFade::m_fade = FADE_NONE;				// フェードの状態
D3DXCOLOR CTitleFade::m_colorFade = D3DXCOLOR(0.0, 0.0, 0.0, 1.0);	// 色情報

//==================================================================================================================
// コンストラクタ
//==================================================================================================================
CTitleFade::CTitleFade(PRIORITY type = CScene::PRIORITY_GAME_FADE) :CScene2D(type)
{

}

//==================================================================================================================
// デストラクタ
//==================================================================================================================
CTitleFade::~CTitleFade()
{

}

//==================================================================================================================
// 初期化処理
//==================================================================================================================
void CTitleFade::Init(void)
{
	// 初期化
	m_pos = D3DXVECTOR3(SCREEN_WIDTH / 2, SCREEN_HEIGHT / 2, 0);			// 位置
	m_fade = FADE_IN;														// フェードの状態
	m_colorFade = D3DXCOLOR(1.0f, 1.0f, 1.0f, 1.0f);						// 白い画面（不透明）

	// Scene2Dの初期化処理
	CScene2D::Init();
}

//==================================================================================================================
// 終了処理
//==================================================================================================================
void CTitleFade::Uninit(void)
{
	// Scene2Dの終了処理
	CScene2D::Uninit();
}

//==================================================================================================================
// 更新処理
//==================================================================================================================
void CTitleFade::Update(void)
{
	// フェードがあるとき
	if (m_fade != FADE_NONE)
	{
		// フェード状態がインのとき
		if (m_fade == FADE_IN)
		{
			// フェードイン処理
			m_colorFade.a -= DCREACE_ALPHA;				// 透明度減らしていく

			// 透明度が0以下だったとき
			if (m_colorFade.a <= 0.0f)
			{
				// フェード処理終了
				m_colorFade.a = 0.0f;					// 透明度を0にする
				m_fade = FADE_NONE;						// フェード状態を何もない状態にする
			}
		}
		else if (m_fade == FADE_OUT)
		{// フェード状態がアウトのとき
			//フェードアウト処理
			m_colorFade.a += DCREACE_ALPHA;				// 画面を不透明にしていく

			// 透明度が1より大きくなったとき
			if (m_colorFade.a >= 1.0f)
			{
				//フェードイン処理に切り替え
				m_colorFade.a = 1.0f;					// 透明度を1にする
			}
		}
	}

	// 色設定
	SetCol(m_colorFade);

	// Scene2Dの更新処理
	CScene2D::Update();
}

//==================================================================================================================
// 描画処理
//==================================================================================================================
void CTitleFade::Draw(void)
{
	// Scene2Dの描画処理
	CScene2D::Draw();
}

//==================================================================================================================
// フェードの取得
//==================================================================================================================
CTitleFade::TITLE_FADE CTitleFade::GetTitleFade(void)
{
	return m_fade;
}

//==================================================================================================================
// オブジェクトの生成
//==================================================================================================================
CTitleFade * CTitleFade::Create(void)
{
	// フェード変数NULLにする
	CTitleFade *pTitleFade = NULL;

	// 動的に確保
	pTitleFade = new CTitleFade(CScene::PRIORITY_GAME_FADE);

	// 初期化処理
	pTitleFade->Init();

	// 値を返す
	return pTitleFade;
}

//==================================================================================================================
// テクスチャ情報ロード
//==================================================================================================================
HRESULT CTitleFade::Load(void)
{
	CRenderer *pRenderer = CManager::GetRenderer();			// レンダラーの情報取得
	LPDIRECT3DDEVICE9 pDevice = pRenderer->GetDevice();		// デバイスを取得する

	// テクスチャの読み込み
	D3DXCreateTextureFromFile(pDevice,						// デバイスへのポインタ
		"data/TEXTURE/",									// ファイルの名前
		&m_pTexture);										// 読み込むメモリー

	return S_OK;											// 値を返す
}

//==================================================================================================================
// テクスチャ情報破棄
//==================================================================================================================
void CTitleFade::Unload(void)
{
	// テクスチャがあるとき
	if (m_pTexture != NULL)
	{
		m_pTexture->Release();					// テクスチャの開放
		m_pTexture = NULL;						// NULLにする
	}
}

//==================================================================================================================
// フェードイン処理
//==================================================================================================================
void CTitleFade::FadeIN(void)
{
	m_fade = FADE_IN;
}

//==================================================================================================================
// フェードアウト処理
//==================================================================================================================
void CTitleFade::FadeOUT(void)
{
	m_fade = FADE_OUT;
	m_colorFade = D3DXCOLOR(1.0f, 1.0f, 1.0f, 0.0);	// 色初期化
}
